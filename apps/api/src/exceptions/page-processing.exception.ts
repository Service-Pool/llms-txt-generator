/**
 * Ошибка обработки отдельной страницы при генерации llms.txt
 *
 * Используется на уровне контекста генерации для ситуаций, специфичных для обработки страниц:
 * - Невозможность извлечь читаемый контент (нет текста, только изображения)
 * - Ошибки AI генерации для конкретной страницы
 * - Проблемы парсинга специфичного контента
 *
 * НЕ включает сетевые ошибки (см. ResourceUnavailableError)
 *
 * При возникновении этой ошибки:
 * - Текущая страница пропускается
 * - Ошибка записывается в Order.errors
 * - Обработка продолжается со следующей страницы
 */
class PageProcessingError extends Error {
	constructor(message: string) {
		super(message);
		this.name = 'PageProcessingError';
	}
}

export { PageProcessingError };
